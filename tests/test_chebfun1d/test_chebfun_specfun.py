class TestExpLog:
    """exp and log: method form, module form, and round-trip."""

    def test_exp_identity(self):
        """exp(identity) == chebfun(exp) pointwise."""
        x = Chebfun.identity()
        f = x.exp()
        g = chebfun(jnp.exp)
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(g(xs)), rtol=RTOL, atol=ATOL
        )

    def test_log_of_exp(self):
        """log(exp(x)) == x."""
        x = Chebfun.identity()
        f = x.exp().log()
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(xs), rtol=RTOL, atol=ATOL,
            err_msg="log(exp(x)) must equal x"
        )

    def test_exp_of_log(self):
        """exp(log(f)) == f when f > 0."""
        # f = 2 + sin(x) is strictly positive on [-1, 1]
        f = chebfun(lambda x: 2.0 + jnp.sin(x))
        g = f.log().exp()
        xs = _pts()
        npt.assert_allclose(
            np.array(g(xs)), np.array(f(xs)), rtol=RTOL, atol=ATOL,
            err_msg="exp(log(f)) must equal f"
        )

    def test_module_exp_log(self):
        """cj.exp and cj.log match method forms."""
        f = chebfun(lambda x: 1.0 + 0.5 * x)
        g_method = f.exp().log()
        g_module = cj.log(cj.exp(f))
        xs = _pts()
        npt.assert_allclose(
            np.array(g_method(xs)), np.array(g_module(xs)), rtol=RTOL, atol=ATOL
        )

    def test_exp_sum_property(self):
        """exp(x + y) == exp(x) * exp(y) via Chebfun arithmetic."""
        x = Chebfun.identity()
        # Use x/2 and x/3 so exp doesn't blow up too much on [-1,1]
        a = (x * 0.5).exp()
        b = (x * (1.0 / 3.0)).exp()
        ab = a * b
        c = (x * (0.5 + 1.0 / 3.0)).exp()
        xs = _pts()
        npt.assert_allclose(
            np.array(ab(xs)), np.array(c(xs)), rtol=RTOL, atol=ATOL
        )


