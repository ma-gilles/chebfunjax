# Tier 6: inverse trig
# ============================================================================


class TestInverseTrig:
    """asin, acos, atan."""

    def test_asin_roundtrip(self):
        """asin(sin(x)) == x for x in [-1, 1]."""
        # sin maps [-pi/2, pi/2] to [-1, 1]; restrict to avoid branch issues
        # Use x/2 so we stay in the invertible range of asin
        x = Chebfun.identity()
        half_x = x * 0.5
        f = half_x.sin().asin()
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(xs * 0.5), rtol=RTOL, atol=ATOL_LOOSE,
            err_msg="asin(sin(x/2)) must equal x/2"
        )

    def test_acos_roundtrip(self):
        """acos(cos(t)) == t for t in [0, 1] (cos is injective on [0, pi])."""
        # Build a Chebfun on [0, 1] representing the identity t -> t.
        # Then acos(cos(t)) should equal t for t in [0, 1] (values in [0, 1] ⊂ [0, pi]).
        t = chebfun(lambda x: x, domain=[0.0, 1.0])
        f = t.cos().acos()
        xs = jnp.linspace(0.05, 0.95, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(f(xs)), np.array(xs),
            rtol=RTOL, atol=ATOL_LOOSE,
            err_msg="acos(cos(t)) must equal t for t in [0, 1]"
        )

    def test_atan_identity(self):
        """atan(identity) == chebfun(arctan)."""
        x = Chebfun.identity()
        f = x.atan()
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(jnp.arctan(xs)), rtol=RTOL, atol=ATOL
        )

    def test_module_asin_acos_atan(self):
        """cj.asin, cj.acos, cj.atan match method forms."""
        # Use a function with range in (-1, 1) for asin/acos
        f = chebfun(lambda x: 0.5 * x)
        xs = _pts()
        npt.assert_allclose(
            np.array(cj.asin(f)(xs)), np.array(f.asin()(xs)), rtol=RTOL, atol=ATOL
        )
        npt.assert_allclose(
            np.array(cj.acos(f)(xs)), np.array(f.acos()(xs)), rtol=RTOL, atol=ATOL
        )
        x = Chebfun.identity()
        npt.assert_allclose(
            np.array(cj.atan(x)(xs)), np.array(x.atan()(xs)), rtol=RTOL, atol=ATOL
        )


