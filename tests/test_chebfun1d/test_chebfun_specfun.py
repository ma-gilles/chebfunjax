# Tier 4: abs and sign (with breakpoint insertion)
# ============================================================================


class TestAbsSign:
    """abs and sign: breakpoint insertion, positivity, round-trip."""

    def test_abs_positive_function(self):
        """abs(f) == f when f > 0 (no breakpoints needed)."""
        f = chebfun(lambda x: 2.0 + jnp.sin(x))
        g = f.abs()
        xs = _pts()
        npt.assert_allclose(
            np.array(g(xs)), np.array(f(xs)), rtol=RTOL, atol=ATOL
        )

    def test_abs_is_nonnegative(self):
        """abs(sin(x)) >= 0 everywhere."""
        f = chebfun(jnp.sin)
        g = f.abs()
        xs = _pts()
        vals = np.array(g(xs))
        assert np.all(vals >= -1e-12), f"abs values have negative: min={vals.min()}"

    def test_abs_matches_reference(self):
        """abs(sin(x)) matches jnp.abs(sin(xs)) pointwise."""
        f = chebfun(jnp.sin)
        g = f.abs()
        xs = _pts()
        npt.assert_allclose(
            np.array(g(xs)),
            np.array(jnp.abs(jnp.sin(xs))),
            rtol=RTOL, atol=ATOL
        )

    def test_module_abs(self):
        """cj.abs(f) == f.abs()."""
        f = chebfun(jnp.sin)
        npt.assert_allclose(
            np.array(cj.abs(f)(_pts())),
            np.array(f.abs()(_pts())),
            rtol=RTOL, atol=ATOL
        )

    def test_sign_values(self):
        """sign(sin(x)) is approximately +1 or -1 on sign-constant pieces.

        Each piece is an adaptive Chebyshev approximation of a step function
        (+1 or -1 constant), so the approximation is accurate to spectral
        precision well away from the breakpoint at zero.
        """
        f = chebfun(jnp.sin)
        g = f.sign()
        # In (0, 1], sin > 0 so sign should be close to +1.
        # The Chebyshev approximation of a constant converges very fast,
        # so we expect ~eps accuracy well away from the breakpoint.
        xs_pos = jnp.linspace(0.1, 0.9, 10, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(g(xs_pos)),
            np.ones(10),
            rtol=1e-4, atol=1e-4,
            err_msg="sign(sin) should be +1 on (0.1, 0.9)"
        )
        # In [-1, 0), sin < 0 so sign should be close to -1.
        xs_neg = jnp.linspace(-0.9, -0.1, 10, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(g(xs_neg)),
            -np.ones(10),
            rtol=1e-4, atol=1e-4,
            err_msg="sign(sin) should be -1 on (-0.9, -0.1)"
        )

    def test_module_sign(self):
        """cj.sign(f) == f.sign()."""
        f = chebfun(jnp.cos)
        npt.assert_allclose(
            np.array(cj.sign(f)(_pts())),
            np.array(f.sign()(_pts())),
            rtol=RTOL, atol=ATOL
        )

    def test_abs_sign_consistency(self):
        """|f| agrees with sign(f)*f when evaluated via the same function.

        ``f.sign() * f`` requires matching domains and is not directly
        supported (sign inserts breakpoints).  Instead verify the identity
        pointwise by evaluating both sides as callables.
        """
        f = chebfun(jnp.sin)
        lhs = f.abs()
        sign_f = f.sign()
        # Evaluate away from the breakpoint at zero
        xs = jnp.linspace(-0.9, -0.1, 15, dtype=jnp.float64)
        lhs_vals = np.array(lhs(xs))
        sign_vals = np.array(sign_f(xs))
        f_vals = np.array(f(xs))
        rhs_vals = sign_vals * f_vals
        npt.assert_allclose(lhs_vals, rhs_vals, rtol=1e-4, atol=1e-4)

        xs2 = jnp.linspace(0.1, 0.9, 15, dtype=jnp.float64)
        lhs_vals2 = np.array(lhs(xs2))
        sign_vals2 = np.array(sign_f(xs2))
        f_vals2 = np.array(f(xs2))
        rhs_vals2 = sign_vals2 * f_vals2
        npt.assert_allclose(lhs_vals2, rhs_vals2, rtol=1e-4, atol=1e-4)


