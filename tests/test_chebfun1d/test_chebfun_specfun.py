"""Tests for special functions applied to Chebfun objects (U44).

All functions are thin wrappers around ``_apply_fun`` which calls
``_Piece.from_function`` adaptively.  Tests verify:

1. Method form:  ``f.sin()`` etc.
2. Module form:  ``cj.sin(f)`` etc.
3. Mathematical round-trips:  ``exp(log(f)) ≈ f``, ``sin^2 + cos^2 ≈ 1``, etc.
4. sin(identity) matches chebfun(sin) directly.
5. abs / sign introduce breakpoints at zeros correctly.
6. Multi-piece Chebfuns are handled piece-by-piece.

JAX contract
------------
- All special functions: NOT JIT-safe (adaptive construction).
- The *result* Chebfun evaluation is JIT/vmap-safe (single-piece case).

MATLAB golden refs: tests/references/chebfun_specfun.mat (generated by
matlab_harness/generate_refs_specfun.m once MATLAB is available).
"""

from __future__ import annotations

import jax.numpy as jnp
import numpy as np
import numpy.testing as npt
import pytest

import chebfunjax as cj
from chebfunjax.chebfun1d.chebfun import Chebfun, chebfun

# Standard tolerances for spectral accuracy (float64).
# Special-function compositions tolerate slightly more error than
# raw polynomial arithmetic because each adaptive build adds ~eps rounding.
RTOL = 1e-12
ATOL = 1e-12

# Looser tolerance for near-singular functions (arcsin/arccos near ±1,
# log near 0, sqrt near 0) where the adaptive algorithm may need more
# coefficients to resolve the boundary singularity.
ATOL_LOOSE = 1e-10


# ============================================================================
# Helpers
# ============================================================================


def _pts(n=40, a=-1.0, b=1.0):
    """Uniformly spaced test points in (a, b), avoiding endpoints."""
    return jnp.linspace(a + 1e-8, b - 1e-8, n, dtype=jnp.float64)


# ============================================================================
# Tier 1: sin, cos — basic transcendentals
# ============================================================================


class TestSinCos:
    """sin and cos: method form, module form, and identities."""

    def test_sin_identity(self):
        """sin(identity) == chebfun(sin) pointwise."""
        x = Chebfun.identity()
        f = x.sin()
        g = chebfun(jnp.sin)
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(g(xs)), rtol=RTOL, atol=ATOL,
            err_msg="sin(identity) must equal chebfun(jnp.sin)"
        )

    def test_cos_identity(self):
        """cos(identity) == chebfun(cos) pointwise."""
        x = Chebfun.identity()
        f = x.cos()
        g = chebfun(jnp.cos)
        xs = _pts()
        npt.assert_allclose(
            np.array(f(xs)), np.array(g(xs)), rtol=RTOL, atol=ATOL,
            err_msg="cos(identity) must equal chebfun(jnp.cos)"
        )

    def test_module_sin(self):
        """cj.sin(f) == f.sin()."""
        f = chebfun(jnp.sin)
        g = cj.sin(f)
        h = f.sin()
        xs = _pts()
        npt.assert_allclose(
            np.array(g(xs)), np.array(h(xs)), rtol=RTOL, atol=ATOL
        )

    def test_module_cos(self):
        """cj.cos(f) == f.cos()."""
        f = chebfun(jnp.cos)
        g = cj.cos(f)
        h = f.cos()
        xs = _pts()
        npt.assert_allclose(
            np.array(g(xs)), np.array(h(xs)), rtol=RTOL, atol=ATOL
        )

    def test_pythagorean_identity(self):
        """sin(x)^2 + cos(x)^2 == 1 to spectral accuracy."""
        x = Chebfun.identity()
        s = x.sin()
        c = x.cos()
        one = s ** 2 + c ** 2
        xs = _pts()
        npt.assert_allclose(
            np.array(one(xs)),
            np.ones(len(xs)),
            rtol=RTOL,
            atol=ATOL,
            err_msg="sin^2 + cos^2 must equal 1"
        )

    def test_sin_of_cos(self):
        """sin(cos(x)) pointwise."""
        x = Chebfun.identity()
        f = x.cos().sin()
        xs = _pts()
        expected = jnp.sin(jnp.cos(xs))
        npt.assert_allclose(
            np.array(f(xs)), np.array(expected), rtol=RTOL, atol=ATOL
        )

    def test_sin_custom_domain(self):
        """sin on a custom domain [0, pi]."""
        f = chebfun(lambda x: x, domain=[0.0, float(jnp.pi)])
        s = f.sin()
        xs = jnp.linspace(0.01, float(jnp.pi) - 0.01, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(s(xs)), np.array(jnp.sin(xs)), rtol=RTOL, atol=ATOL
        )


