class TestChebfunSum:
    """Tests for Chebfun.sum().

    JAX contract: jit=yes.
    """

    def test_sin_integral_on_symmetric_domain(self):
        """int_{-1}^{1} sin(x) dx == 0 (odd function)."""
        f = chebfun(jnp.sin)
        result = float(f.sum())
        npt.assert_allclose(result, 0.0, atol=1e-13)

    def test_cos_integral(self):
        """int_{-1}^{1} cos(x) dx == 2*sin(1)."""
        f = chebfun(jnp.cos)
        expected = 2.0 * float(jnp.sin(jnp.float64(1.0)))
        result = float(f.sum())
        npt.assert_allclose(result, expected, rtol=1e-12)

    def test_exp_integral(self):
        """int_{-1}^{1} exp(x) dx == e - 1/e."""
        f = chebfun(jnp.exp)
        expected = float(jnp.exp(jnp.float64(1.0))) - float(jnp.exp(jnp.float64(-1.0)))
        result = float(f.sum())
        npt.assert_allclose(result, expected, rtol=1e-12)

    def test_sin_sq_integral(self):
        """int_{-1}^{1} sin^2(x) dx = 1 - sin(2)/2  (= 1 - sin(1)*cos(1))."""
        f = chebfun(jnp.sin)
        h = f * f
        result = float(h.sum())
        expected = float(1.0 - jnp.sin(jnp.float64(2.0)) / 2.0)
        npt.assert_allclose(result, expected, rtol=1e-12)

    def test_constant_integral(self):
        """int_{-1}^{1} 3 dx == 6."""
        f = chebfun(3.0)
        result = float(f.sum())
        npt.assert_allclose(result, 6.0, rtol=1e-13)

    def test_sum_custom_domain(self):
        """int_{0}^{pi} sin(x) dx == 2."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        result = float(f.sum())
        npt.assert_allclose(result, 2.0, rtol=1e-12)

    def test_sum_multipiece(self):
        """Piecewise integral == sum of piece integrals."""
        f_one = chebfun(jnp.exp)
        f_two = chebfun(jnp.exp, domain=[-1, 0, 1])
        npt.assert_allclose(
            float(f_one.sum()), float(f_two.sum()), rtol=1e-12
        )

    def test_sum_jit(self):
        """sum() is JIT-safe."""
        f = chebfun(jnp.cos)
        jit_sum = jax.jit(lambda: f.sum())
        result = float(jit_sum())
        expected = 2.0 * float(jnp.sin(jnp.float64(1.0)))
        npt.assert_allclose(result, expected, rtol=1e-12)


