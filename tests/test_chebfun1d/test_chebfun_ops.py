class TestChebfunInnerNorm:
    """Tests for Chebfun.inner() and Chebfun.norm().

    JAX contract: jit=yes.
    """

    def test_inner_sin_sin(self):
        """<sin, sin> == int_{-1}^{1} sin^2(x) dx."""
        f = chebfun(jnp.sin)
        result = float(f.inner(f))
        expected = float((f * f).sum())
        npt.assert_allclose(result, expected, rtol=1e-12)

    def test_inner_sin_cos_is_zero(self):
        r"""<sin, cos> = int_{-1}^{1} sin(x)cos(x) dx = 0.

        By symmetry: sin(x)*cos(x) = sin(2x)/2, which is an odd function,
        so its integral over a symmetric domain is 0.
        """
        f = chebfun(jnp.sin)
        g = chebfun(jnp.cos)
        result = float(f.inner(g))
        npt.assert_allclose(result, 0.0, atol=1e-13)

    def test_inner_domain_mismatch_raises(self):
        """inner() with different domains should raise ValueError."""
        f = chebfun(jnp.sin)
        g = chebfun(jnp.sin, domain=[0, 1])
        with pytest.raises(ValueError, match="domains do not match"):
            f.inner(g)

    def test_norm2_sin(self):
        """||sin||_2^2 == <sin, sin> == integral of sin^2."""
        f = chebfun(jnp.sin)
        n = float(f.norm(2))
        expected = float(jnp.sqrt(f.inner(f)))
        npt.assert_allclose(n, expected, rtol=1e-12)

    def test_norm2_positive(self):
        """L2 norm is positive."""
        f = chebfun(jnp.sin)
        assert float(f.norm()) > 0

    def test_norm_inf(self):
        """L-inf norm of sin is max|sin| ~ 0.8415."""
        f = chebfun(jnp.sin)
        n_inf = float(f.norm(jnp.inf))
        # max of |sin| on [-1, 1] is sin(1)
        expected = float(jnp.sin(jnp.float64(1.0)))
        npt.assert_allclose(n_inf, expected, rtol=1e-3)

    def test_norm_custom_domain(self):
        """||sin||_2 on [0, pi]."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        n = float(f.norm())
        # int_0^pi sin^2 dx = pi/2
        expected = float(jnp.sqrt(jnp.pi / 2.0))
        npt.assert_allclose(n, expected, rtol=1e-10)


