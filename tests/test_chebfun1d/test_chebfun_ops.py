class TestChebfunCumsum:
    """Tests for Chebfun.cumsum().

    JAX contract: jit=yes.
    """

    def test_cumsum_of_cos_is_sin_minus_sin_a(self):
        """cumsum(cos) on [-1, 1] should be sin(x) - sin(-1)."""
        f = chebfun(jnp.cos)
        F = f.cumsum()
        xs = jnp.linspace(-1.0, 1.0, 40, dtype=jnp.float64)
        expected = jnp.sin(xs) - jnp.sin(jnp.float64(-1.0))
        npt.assert_allclose(
            np.array(F(xs)), np.array(expected), rtol=RTOL, atol=ATOL
        )

    def test_cumsum_left_endpoint_is_zero(self):
        """F(a) = 0 for any Chebfun."""
        f = chebfun(jnp.sin)
        F = f.cumsum()
        val = float(F(jnp.float64(-1.0)))
        npt.assert_allclose(val, 0.0, atol=1e-13)

    def test_cumsum_custom_domain(self):
        """cumsum of sin on [0, pi]: F(x) = -cos(x) + cos(0) = 1 - cos(x)."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        F = f.cumsum()
        xs = jnp.linspace(0.0, float(jnp.pi), 30, dtype=jnp.float64)
        expected = 1.0 - jnp.cos(xs)
        npt.assert_allclose(
            np.array(F(xs)), np.array(expected), rtol=RTOL, atol=ATOL
        )

    def test_cumsum_then_diff_recovers_f(self):
        """(cumsum(f)).diff() == f pointwise (fundamental theorem of calculus)."""
        f = chebfun(jnp.exp)
        F = f.cumsum()
        Fp = F.diff()
        xs = jnp.linspace(-0.9, 0.9, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(Fp(xs)), np.array(jnp.exp(xs)), rtol=1e-10, atol=1e-10
        )

    def test_cumsum_multipiece_continuity(self):
        """Piecewise cumsum should be continuous at breakpoints.

        We check by evaluating F at the breakpoint x=0 from both pieces
        (using endpoint_values) and verifying they agree to machine precision.
        """
        f = chebfun(jnp.cos, domain=[-1, 0, 1])
        F = f.cumsum()
        # Right endpoint of piece 0 and left endpoint of piece 1 should match
        p0 = F.funs[0]
        p1 = F.funs[1]
        _, rval = p0.endpoint_values
        lval, _ = p1.endpoint_values
        npt.assert_allclose(rval, lval, atol=1e-13)

    def test_cumsum_multipiece_left_endpoint_zero(self):
        """Piecewise cumsum: F(a) = 0 at the global left endpoint."""
        f = chebfun(jnp.sin, domain=[-1, 0, 1])
        F = f.cumsum()
        val = float(F(jnp.float64(-1.0)))
        npt.assert_allclose(val, 0.0, atol=1e-13)


