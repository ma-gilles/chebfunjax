class TestChebfunExtrema:
    """Tests for Chebfun.max(), Chebfun.min(), Chebfun.minandmax().

    JAX contract: jit=NO (uses roots of derivative).
    """

    def test_max_of_sin_on_zero_pi(self):
        """max of sin on [0, pi] is 1 at x = pi/2."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        x_max, f_max = f.max()
        npt.assert_allclose(f_max, 1.0, atol=1e-10)
        npt.assert_allclose(x_max, float(jnp.pi / 2), atol=1e-9)

    def test_min_of_cos_on_zero_pi(self):
        """min of cos on [0, pi] is -1 at x = pi."""
        f = chebfun(jnp.cos, domain=[0, jnp.pi])
        x_min, f_min = f.min()
        npt.assert_allclose(f_min, -1.0, atol=1e-10)
        npt.assert_allclose(x_min, float(jnp.pi), atol=1e-9)

    def test_minandmax_sin(self):
        """minandmax of sin on [-1, 1] gives -sin(1) and sin(1)."""
        f = chebfun(jnp.sin)
        (x_min, f_min), (x_max, f_max) = f.minandmax()
        sin1 = float(jnp.sin(jnp.float64(1.0)))
        npt.assert_allclose(f_min, -sin1, atol=1e-10)
        npt.assert_allclose(f_max, sin1, atol=1e-10)

    def test_min_max_consistency(self):
        """min and max should be consistent with minandmax."""
        f = chebfun(jnp.cos, domain=[0, jnp.pi])
        (x_min, f_min), (x_max, f_max) = f.minandmax()
        x_min2, f_min2 = f.min()
        x_max2, f_max2 = f.max()
        npt.assert_allclose(f_min, f_min2, atol=1e-14)
        npt.assert_allclose(f_max, f_max2, atol=1e-14)


