class TestChebfunDiff:
    """Tests for Chebfun.diff().

    JAX contract: jit=yes (k static), vmap=yes.
    """

    def test_sin_diff_matches_cos(self):
        """d/dx [sin(x)] ≈ cos(x) at machine precision."""
        f = chebfun(jnp.sin)
        fp = f.diff()
        xs = jnp.linspace(-1.0, 1.0, 40, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fp(xs)), np.array(jnp.cos(xs)), rtol=RTOL, atol=ATOL
        )

    def test_cos_diff_matches_neg_sin(self):
        """d/dx [cos(x)] == -sin(x)."""
        f = chebfun(jnp.cos)
        fp = f.diff()
        xs = jnp.linspace(-1.0, 1.0, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fp(xs)), np.array(-jnp.sin(xs)), rtol=RTOL, atol=ATOL
        )

    def test_exp_diff_matches_exp(self):
        """d/dx [exp(x)] == exp(x)."""
        f = chebfun(jnp.exp)
        fp = f.diff()
        xs = jnp.linspace(-1.0, 1.0, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fp(xs)), np.array(jnp.exp(xs)), rtol=RTOL, atol=ATOL
        )

    def test_second_derivative(self):
        """d^2/dx^2 [sin(x)] == -sin(x)."""
        f = chebfun(jnp.sin)
        fpp = f.diff(2)
        xs = jnp.linspace(-1.0, 1.0, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fpp(xs)), np.array(-jnp.sin(xs)), rtol=RTOL, atol=ATOL
        )

    def test_diff_at_specific_point(self):
        """f.diff()(0.5) ≈ cos(0.5) — spec example from task."""
        f = chebfun(jnp.sin)
        dfdx = f.diff()
        npt.assert_allclose(
            float(dfdx(jnp.float64(0.5))),
            float(jnp.cos(jnp.float64(0.5))),
            rtol=1e-13,
        )

    def test_diff_zeroth_order_is_identity(self):
        """diff(0) returns self (no-op)."""
        f = chebfun(jnp.sin)
        g = f.diff(0)
        xs = jnp.linspace(-1.0, 1.0, 10, dtype=jnp.float64)
        npt.assert_allclose(np.array(g(xs)), np.array(f(xs)), atol=1e-15)

    def test_diff_custom_domain(self):
        """d/dx [sin(x)] on [0, pi] should match cos(x)."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        fp = f.diff()
        xs = jnp.linspace(0.0, float(jnp.pi), 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fp(xs)), np.array(jnp.cos(xs)), rtol=RTOL, atol=ATOL
        )

    def test_diff_multipiece(self):
        """Piecewise Chebfun diff should match pointwise derivative."""
        f = chebfun(jnp.sin, domain=[-1, 0, 1])
        fp = f.diff()
        xs = jnp.array([-0.8, -0.5, -0.2, 0.2, 0.5, 0.8], dtype=jnp.float64)
        npt.assert_allclose(
            np.array(fp(xs)), np.array(jnp.cos(xs)), rtol=1e-10, atol=1e-10
        )

    def test_diff_jit(self):
        """Differentiation result is JIT-safe."""
        import equinox as eqx
        f = chebfun(jnp.sin)
        fp = f.diff()
        jit_fp = eqx.filter_jit(fp)
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_fp(xs)), np.array(jnp.cos(xs)), rtol=RTOL, atol=ATOL
        )


