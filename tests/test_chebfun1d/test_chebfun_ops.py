class TestChebfunComposed:
    """Composed operation integration tests."""

    def test_sin_sq_norm(self):
        """||sin||_2^2 = int sin^2 dx = 1 - sin(2)/2."""
        f = chebfun(jnp.sin)
        n_sq = float(f.norm() ** 2)
        expected = float(1.0 - jnp.sin(jnp.float64(2.0)) / 2.0)
        npt.assert_allclose(n_sq, expected, rtol=1e-11)

    def test_fundamental_theorem_of_calculus(self):
        """int_a^b f'(x) dx = f(b) - f(a)."""
        f = chebfun(jnp.exp)
        fp = f.diff()
        integral = float(fp.sum())
        expected = float(jnp.exp(jnp.float64(1.0))) - float(jnp.exp(jnp.float64(-1.0)))
        npt.assert_allclose(integral, expected, rtol=1e-12)

    def test_sin_plus_cos_sum(self):
        """int_{-1}^{1} (sin + cos)(x) dx = 2*sin(1)."""
        f = chebfun(jnp.sin)
        g = chebfun(jnp.cos)
        h = f + g
        result = float(h.sum())
        expected = float(2.0 * jnp.sin(jnp.float64(1.0)))
        npt.assert_allclose(result, expected, rtol=1e-12)

    def test_diff_sin_roots(self):
        """Roots of d(sin^2)/dx = 2*sin*cos = sin(2x) on [-1,1] are {-pi/4?, 0, pi/4?}.

        sin^2 has extrema where 2*sin(x)*cos(x)=0 i.e. x=0, ±1.
        On [-1, 1] these are x=-1, 0, 1.
        """
        f = chebfun(jnp.sin)
        g = f * f  # sin^2
        gp = g.diff()  # 2*sin*cos = sin(2x)
        r = np.array(gp.roots())
        # Roots of sin(2x) in [-1, 1] are 0 (and endpoints ±1 approximately)
        # 2x = 0 => x=0, ±pi/2 ~ ±1.5708 (outside [-1,1])
        assert r.shape[0] >= 1
        npt.assert_allclose(np.sort(r)[len(r) // 2], 0.0, atol=1e-10)

    def test_chebfun_from_arithmetic_is_correct_callable(self):
        """The result of arithmetic is a proper Chebfun (not just coefficients)."""
        f = chebfun(jnp.sin)
        g = chebfun(jnp.cos)
        h = f + g
        assert isinstance(h, Chebfun)
        assert h.ishappy


