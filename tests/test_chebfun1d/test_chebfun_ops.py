class TestChebfunRoots:
    """Tests for Chebfun.roots().

    JAX contract: jit=NO (variable output size).
    """

    def test_sin_root_at_zero(self):
        """sin has a root at 0 on [-1, 1]."""
        f = chebfun(jnp.sin)
        r = np.array(f.roots())
        npt.assert_allclose(r, [0.0], atol=1e-12)

    def test_sin_roots_on_minus_pi_to_pi(self):
        """sin has roots at -pi, 0, pi on [-pi, pi]."""
        f = chebfun(jnp.sin, domain=[-jnp.pi, jnp.pi])
        r = np.array(f.roots())
        expected = np.array([-float(jnp.pi), 0.0, float(jnp.pi)])
        npt.assert_allclose(r, expected, atol=1e-10)

    def test_cos_roots_on_minus_pi_to_pi(self):
        """cos has roots at -pi/2 and pi/2 on [-pi, pi]."""
        f = chebfun(jnp.cos, domain=[-jnp.pi, jnp.pi])
        r = np.array(f.roots())
        expected = np.sort([-float(jnp.pi / 2), float(jnp.pi / 2)])
        assert len(r) == 2
        npt.assert_allclose(r, expected, atol=1e-10)

    def test_poly_roots(self):
        """x^2 - 0.25 has roots at ±0.5."""
        x_id = Chebfun.identity()
        h = x_id ** 2 - 0.25
        r = np.array(h.roots())
        npt.assert_allclose(np.sort(r), [-0.5, 0.5], atol=1e-12)

    def test_no_roots_in_domain(self):
        """exp(x) > 0, so no roots."""
        f = chebfun(jnp.exp)
        r = np.array(f.roots())
        assert r.shape[0] == 0, f"Expected 0 roots, got {r}"

    def test_roots_multipiece(self):
        """Roots from a two-piece Chebfun are collected and deduplicated.

        sin on [-pi, 0, pi] (two pieces) has roots at -pi, 0, pi.
        The root at 0 is a breakpoint shared between the two pieces;
        it should appear only once in the output.
        """
        f = chebfun(jnp.sin, domain=[-jnp.pi, 0, jnp.pi])
        r = np.array(f.roots())
        expected = np.array([-float(jnp.pi), 0.0, float(jnp.pi)])
        assert len(r) == 3, f"Expected 3 roots (no duplicates), got {len(r)}: {r}"
        npt.assert_allclose(r, expected, atol=1e-10)


