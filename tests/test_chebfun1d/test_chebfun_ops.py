    def load_refs(self, request):
        """Load MATLAB references, skip if not available."""
        from pathlib import Path

        import scipy.io

        ref_path = Path(__file__).parent.parent / "references" / "chebfun_ops.mat"
        if not ref_path.exists():
            pytest.skip(
                f"MATLAB golden ref chebfun_ops.mat not found at {ref_path}. "
                f"Generate with matlab_harness/generate_refs_ops.m."
            )
