    def test_pow_integer(self):
        """sin^2 == sin * sin."""
        f = chebfun(jnp.sin)
        h = f ** 2
        xs = jnp.linspace(-1.0, 1.0, 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(xs)), np.array(jnp.sin(xs) ** 2), rtol=RTOL, atol=ATOL
        )

    def test_pow_zero(self):
        """sin^0 == 1."""
        f = chebfun(jnp.sin)
        h = f ** 0
        xs = jnp.linspace(-1.0, 1.0, 10, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(xs)), np.ones(10), atol=1e-14
        )

    def test_pow_chebfun(self):
        """exp^sin should give exp(x)^sin(x)."""
        f = chebfun(jnp.exp)
        g = chebfun(jnp.sin)
        h = f ** g
        xs = jnp.linspace(-0.5, 0.5, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(xs)),
            np.array(jnp.exp(xs) ** jnp.sin(xs)),
            rtol=1e-10,
            atol=1e-10,
        )

    def test_abs(self):
        """abs(sin) is non-negative and returns a Chebfun.

        Note: abs(sin) has a kink at sin's zeros (x=0 on [-1,1]).
        The current implementation evaluates on a fine grid and re-interpolates
        (from Chebtech2.__abs__), so the result is a smooth polynomial
        approximation to a non-smooth function.  Accuracy is limited by the
        kink and is NOT expected to be spectral.

        This test checks:
          1. The result is a Chebfun.
          2. All values are non-negative.
          3. At the left and right endpoints (far from the kink), it matches
             |sin| closely.
        """
        f = chebfun(jnp.sin)
        h = abs(f)
        assert isinstance(h, Chebfun)
        # Non-negativity
        xs = jnp.linspace(-1.0, 1.0, 40, dtype=jnp.float64)
        vals = np.array(h(xs))
        assert np.all(vals >= -1e-5), f"abs(sin) has negative values: min={vals.min()}"
        # At endpoints (far from the zero at x=0), match should be decent
        x_end = jnp.array([-1.0, 1.0], dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(x_end)),
            np.abs(np.array(jnp.sin(x_end))),
            rtol=1e-5,
            atol=1e-5,
        )

    def test_domain_mismatch_raises(self):
        """Adding two Chebfuns on different domains should raise ValueError."""
        f = chebfun(jnp.sin, domain=[-1, 1])
        g = chebfun(jnp.sin, domain=[0, 1])
        with pytest.raises(ValueError, match="domains do not match"):
            _ = f + g

    def test_multipiece_add(self):
        """Adding two 2-piece Chebfuns piece-wise."""
        f = chebfun(jnp.sin, domain=[-1, 0, 1])
        g = chebfun(jnp.cos, domain=[-1, 0, 1])
        h = f + g
        assert len(h.funs) == 2
        xs = jnp.linspace(-1.0, 1.0, 40, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(xs)),
            np.array(jnp.sin(xs) + jnp.cos(xs)),
            rtol=RTOL,
            atol=ATOL,
        )

    def test_custom_domain_arithmetic(self):
        """Arithmetic on [0, pi] domain should work correctly."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        g = chebfun(jnp.cos, domain=[0, jnp.pi])
        h = f + g
        xs = jnp.linspace(0.0, float(jnp.pi), 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(h(xs)),
            np.array(jnp.sin(xs) + jnp.cos(xs)),
            rtol=RTOL,
            atol=ATOL,
        )

    # JAX contract tests for arithmetic
    def test_add_jit(self):
        """Addition result is JIT-safe (single-piece)."""
        import equinox as eqx
        f = chebfun(jnp.sin)
        g = chebfun(jnp.cos)
        h = f + g
        jit_h = eqx.filter_jit(h)
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_h(xs)),
            np.array((f + g)(xs)),
            atol=1e-14,
        )

    def test_mul_jit(self):
        """Multiplication result is JIT-safe (single-piece)."""
        import equinox as eqx
        f = chebfun(jnp.sin)
        g = chebfun(jnp.cos)
        h = f * g
        jit_h = eqx.filter_jit(h)
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_h(xs)),
            np.array(jnp.sin(xs) * jnp.cos(xs)),
            rtol=RTOL,
            atol=ATOL,
        )


