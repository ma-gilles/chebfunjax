class TestChebfunProperties:
    """Tests for Chebfun properties: domain, coeffs, values, vscale, ishappy."""

    def test_len_sin(self):
        """len(f) == 14 for sin on [-1, 1]."""
        f = chebfun(jnp.sin)
        assert len(f) == 14

    def test_domain_default(self):
        """Default domain is [-1, 1]."""
        f = chebfun(jnp.sin)
        assert f.domain == Domain((-1.0, 1.0))

    def test_domain_custom(self):
        """Custom domain is stored correctly."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        assert abs(f.domain.a - 0.0) < 1e-14
        assert abs(f.domain.b - float(jnp.pi)) < 1e-12

    def test_vscale_sin(self):
        """vscale of sin should be close to 1 (max |sin| on [-1,1])."""
        f = chebfun(jnp.sin)
        vs = f.vscale
        # |sin(x)| <= 1 on [-1,1], max at x=1: sin(1) ~ 0.8415
        npt.assert_allclose(vs, float(jnp.max(jnp.abs(jnp.sin(
            jnp.linspace(-1.0, 1.0, 1000, dtype=jnp.float64)
        )))), rtol=1e-3)

    def test_ishappy_sin(self):
        """sin on [-1, 1] should be happy (converged)."""
        f = chebfun(jnp.sin)
        assert f.ishappy

    def test_coeffs_length(self):
        """coeffs array length matches len(f)."""
        f = chebfun(jnp.sin)
        assert f.coeffs.shape[0] == len(f)

    def test_values_length(self):
        """values array length matches len(f) for single-piece."""
        f = chebfun(jnp.sin)
        assert f.values.shape[0] == len(f)

    def test_values_consistent_with_coeffs(self):
        """values should be recoverable from coeffs via coeffs2vals."""
        from chebfunjax.utils.transforms import coeffs2vals
        f = chebfun(jnp.sin)
        v_from_coeffs = coeffs2vals(f.coeffs)
        npt.assert_allclose(
            np.array(f.values), np.array(v_from_coeffs), atol=1e-14
        )


