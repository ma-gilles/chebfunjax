class TestChebfunRestrict:
    """Tests for Chebfun.restrict."""

    def test_restrict_half_interval(self):
        """Restricting sin to [0, 1] should give correct values."""
        f = chebfun(jnp.sin)
        g = f.restrict(0.0, 1.0)
        xs = jnp.linspace(0.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(g(xs)), np.array(jnp.sin(xs)), atol=1e-12
        )

    def test_restrict_domain_recorded(self):
        """Restricted Chebfun should have the new domain."""
        f = chebfun(jnp.sin)
        g = f.restrict(0.0, 1.0)
        assert abs(g.domain.a - 0.0) < 1e-14
        assert abs(g.domain.b - 1.0) < 1e-14

    def test_restrict_full_interval(self):
        """Restricting to the full domain should return a copy."""
        f = chebfun(jnp.sin)
        g = f.restrict(-1.0, 1.0)
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(g(xs)), np.array(f(xs)), atol=1e-14
        )

    def test_restrict_outside_raises(self):
        """Restricting outside the domain should raise ValueError."""
        f = chebfun(jnp.sin)
        with pytest.raises(ValueError, match="sub-interval"):
            f.restrict(-2.0, 1.0)


