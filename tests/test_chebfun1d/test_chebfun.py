@pytest.mark.matlab
class TestMATLABGolden:
    """Comparison against MATLAB Chebfun golden references.

    Requires tests/references/chebfun.mat. These are generated by
    matlab_harness/generate_refs.m and committed to the repo.

    Skip this class if the reference file is not present.

    JAX contract: not applicable (offline comparison).
    """

    @pytest.fixture(autouse=True)
    def load_refs(self, request):
        """Load MATLAB references, skip if not available."""
        from pathlib import Path
        import scipy.io

        ref_path = Path(__file__).parent.parent / "references" / "chebfun.mat"
        if not ref_path.exists():
            pytest.skip(
                f"MATLAB golden ref chebfun.mat not found at {ref_path}. "
                f"Generate with matlab_harness/generate_refs.m."
            )
        data = scipy.io.loadmat(str(ref_path), squeeze_me=True)
        self.refs = {k: v for k, v in data.items() if not k.startswith("__")}

    def test_sin_coeffs_vs_matlab(self):
        """sin coefficients should match MATLAB's chebfun(sin)."""
        f = chebfun(jnp.sin)
        mat_coeffs = self.refs["sin_coeffs"]
        npt.assert_allclose(
            np.array(f.coeffs),
            np.asarray(mat_coeffs),
            rtol=1e-12,
            atol=1e-14,
        )

    def test_sin_n_vs_matlab(self):
        """Number of coefficients for sin should match MATLAB."""
        f = chebfun(jnp.sin)
        mat_n = int(self.refs["sin_n"])
        assert len(f) == mat_n

    def test_exp_n_vs_matlab(self):
        """Number of coefficients for exp should match MATLAB."""
        f = chebfun(jnp.exp)
        mat_n = int(self.refs["exp_n"])
        assert len(f) == mat_n

    def test_eval_vs_matlab(self):
        """Evaluation at MATLAB test points should match MATLAB values."""
        f = chebfun(jnp.sin)
        xs = jnp.asarray(self.refs["eval_pts"], dtype=jnp.float64)
        expected = np.asarray(self.refs["sin_eval_vals"])
        npt.assert_allclose(
            np.array(f(xs)), expected, rtol=1e-12, atol=1e-14
        )
