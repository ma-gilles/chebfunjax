class TestChebfunEvaluation:
    """Tests for Chebfun.__call__ evaluation.

    JAX contract: jit=yes (single-piece), vmap=yes, grad=yes.
    """

    def test_sin_at_point(self):
        """sin(0.5) at machine precision."""
        f = chebfun(jnp.sin)
        y = float(f(jnp.float64(0.5)))
        expected = float(jnp.sin(jnp.float64(0.5)))
        npt.assert_allclose(y, expected, rtol=RTOL, atol=ATOL)

    def test_sin_at_array(self):
        """sin at 50 points should match jnp.sin at rtol=1e-12."""
        f = chebfun(jnp.sin)
        xs = jnp.linspace(-1.0, 1.0, 50, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(f(xs)), np.array(jnp.sin(xs)), rtol=RTOL, atol=ATOL
        )

    def test_sin_endpoints(self):
        """Evaluate at domain endpoints."""
        f = chebfun(jnp.sin)
        npt.assert_allclose(
            float(f(jnp.float64(-1.0))), float(jnp.sin(jnp.float64(-1.0))), rtol=1e-12
        )
        npt.assert_allclose(
            float(f(jnp.float64(1.0))), float(jnp.sin(jnp.float64(1.0))), rtol=1e-12
        )

    def test_custom_domain_eval(self):
        """chebfun(jnp.sin, domain=[0, pi]) evaluated at pi/2 should be 1."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        y = float(f(jnp.float64(jnp.pi / 2)))
        npt.assert_allclose(y, 1.0, atol=1e-12)

    def test_custom_domain_multiple_points(self):
        """chebfun on [0, pi] vs jnp.sin at 30 points."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        xs = jnp.linspace(0.0, float(jnp.pi), 30, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(f(xs)), np.array(jnp.sin(xs)), atol=1e-12
        )

    def test_exp_eval(self):
        """exp Chebfun at several points."""
        f = chebfun(jnp.exp)
        xs = jnp.array([-0.7, -0.3, 0.0, 0.3, 0.7], dtype=jnp.float64)
        npt.assert_allclose(
            np.array(f(xs)), np.array(jnp.exp(xs)), rtol=1e-13
        )

    def test_scalar_input(self):
        """Scalar input (0-d array) should return scalar."""
        f = chebfun(jnp.sin)
        x = jnp.float64(0.5)
        y = f(x)
        assert y.ndim == 0

    def test_multipiece_eval(self):
        """Two-piece Chebfun should evaluate correctly across the join."""
        f = chebfun(jnp.sin, domain=[-1, 0, 1])
        xs = jnp.linspace(-1.0, 1.0, 50, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(f(xs)), np.array(jnp.sin(xs)), atol=1e-12
        )

    # ------------------------------------------------------------------
    # JAX contract tests
    # ------------------------------------------------------------------

    def test_jit(self):
        """Single-piece evaluation is JIT-safe via eqx.filter_jit.

        Note: the correct equinox pattern is ``eqx.filter_jit(f)`` rather than
        bare ``jax.jit(f)``, because the latter tries to hash the JAX arrays
        stored in the module (which are dynamic leaves, not static).
        See docs/jax-contract.md.
        """
        import equinox as eqx
        f = chebfun(jnp.sin)
        jit_f = eqx.filter_jit(f)
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_f(xs)), np.array(jnp.sin(xs)), rtol=1e-12
        )

    def test_jit_via_lambda(self):
        """jax.jit(lambda x: f(x)) is also JIT-safe for single-piece."""
        f = chebfun(jnp.sin)
        jit_f = jax.jit(lambda x: f(x))
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_f(xs)), np.array(jnp.sin(xs)), rtol=1e-12
        )

    def test_vmap(self):
        """Single-piece evaluation is vmap-safe via jax.vmap(lambda x: f(x))."""
        f = chebfun(jnp.sin)
        vmapped = jax.vmap(lambda x: f(jnp.atleast_1d(x))[0])
        xs = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(vmapped(xs)), np.array(jnp.sin(xs)), rtol=1e-12
        )

    def test_grad(self):
        """d/dx [f(x)] at x=0.5 should match jnp.cos(0.5) approximately."""
        f = chebfun(jnp.sin)
        grad_f = jax.grad(lambda x: f(jnp.atleast_1d(x))[0])
        dfdx = float(grad_f(jnp.float64(0.5)))
        expected = float(jnp.cos(jnp.float64(0.5)))
        # Gradient through Clenshaw should match cos to at least 1e-10
        npt.assert_allclose(dfdx, expected, rtol=1e-10)

    def test_jit_custom_domain(self):
        """JIT on a Chebfun with a non-standard domain (via eqx.filter_jit)."""
        import equinox as eqx
        f = chebfun(jnp.sin, domain=[0.0, jnp.pi])
        jit_f = eqx.filter_jit(f)
        xs = jnp.linspace(0.0, float(jnp.pi), 20, dtype=jnp.float64)
        npt.assert_allclose(
            np.array(jit_f(xs)), np.array(jnp.sin(xs)), atol=1e-12
        )


