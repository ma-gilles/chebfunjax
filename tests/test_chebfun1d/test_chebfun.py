"""Tests for chebfunjax.chebfun1d.chebfun — Chebfun class and factory.

JAX contract
------------
- f(x)             jit=yes (single-piece), vmap=yes, grad=yes
- f(x)             jit=NO  (multi-piece, Python dispatch)
- chebfun(func)    jit=NO  (adaptive construction, Python loop)

MATLAB golden refs: tests/references/chebfun.mat  (generated by
matlab_harness/generate_refs.m once MATLAB is available).
"""

