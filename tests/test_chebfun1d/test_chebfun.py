import chebfunjax as cj
from chebfunjax.chebfun1d.chebfun import Chebfun, _Piece, chebfun
from chebfunjax.domain import Domain

# Tolerances from project.conf
RTOL = 1e-12
ATOL = 1e-14


# ============================================================================
# Tier 1: Construction — mathematical correctness
# ============================================================================


class TestChebfunConstruction:
    """Tests for the chebfun() factory and Chebfun construction methods.

    JAX contract: jit=NO (adaptive construction uses Python loop).
    """

    def test_sin_default_domain(self):
        """f = chebfun(jnp.sin) on [-1, 1] should give 14 coefficients."""
        f = chebfun(jnp.sin)
        assert f.ishappy
        assert len(f) == 14, f"Expected 14 coefficients for sin on [-1,1], got {len(f)}"

    def test_sin_custom_domain(self):
        """f = chebfun(jnp.sin, domain=[0, jnp.pi]) should work."""
        f = chebfun(jnp.sin, domain=[0, jnp.pi])
        assert f.ishappy
        assert len(f) > 0
        # The domain should be recorded
        assert abs(f.domain.a - 0.0) < 1e-14
        assert abs(f.domain.b - float(jnp.pi)) < 1e-12

    def test_exp_adaptive(self):
        """chebfun(jnp.exp) should converge adaptively."""
        f = chebfun(jnp.exp)
        assert f.ishappy

    def test_constant_scalar(self):
        """chebfun(3.14) should give a constant function."""
        f = chebfun(3.14)
        assert f.ishappy
        x = jnp.linspace(-1.0, 1.0, 20, dtype=jnp.float64)
        npt.assert_allclose(np.array(f(x)), 3.14, atol=1e-14)

    def test_fixed_n(self):
        """chebfun(jnp.sin, n=20) should give exactly 20 coefficients."""
        f = chebfun(jnp.sin, n=20)
        assert len(f) == 20

    def test_from_coeffs_roundtrip(self):
        """Chebfun.from_coeffs should store and return the given coefficients."""
        c = jnp.array([1.0, 0.5, -0.25, 0.1], dtype=jnp.float64)
        f = Chebfun.from_coeffs(c)
        npt.assert_allclose(np.array(f.coeffs), np.array(c), atol=1e-15)

    def test_from_coeffs_default_domain(self):
        """from_coeffs with no domain defaults to [-1, 1]."""
        c = jnp.array([1.0, 0.0, -0.5], dtype=jnp.float64)
        f = Chebfun.from_coeffs(c)
        assert f.domain == Domain((-1.0, 1.0))

    def test_from_values_roundtrip(self):
        """from_values -> coeffs -> values should match."""
        from chebfunjax.utils.quadrature import chebpts
        n = 10
        x = chebpts(n)
        v = jnp.sin(x)
        f = Chebfun.from_values(v)
        npt.assert_allclose(np.array(f.values), np.array(v), atol=1e-14)

    def test_from_coeffs_custom_domain(self):
        """from_coeffs on [0, 2] should work."""
        c = jnp.array([1.0, 0.5], dtype=jnp.float64)
        dom = Domain((0.0, 2.0))
        f = Chebfun.from_coeffs(c, domain=dom)
        assert f.domain == dom

    def test_identity(self):
        """Chebfun.identity() should give f(x) = x on [-1, 1]."""
        x_id = Chebfun.identity()
        xs = jnp.array([-1.0, -0.5, 0.0, 0.5, 1.0], dtype=jnp.float64)
        npt.assert_allclose(np.array(x_id(xs)), np.array(xs), atol=1e-14)

    def test_identity_custom_domain(self):
        """Chebfun.identity(Domain((0, 2))) should give f(x) = x on [0, 2]."""
        dom = Domain((0.0, 2.0))
        x_id = Chebfun.identity(dom)
        xs = jnp.array([0.0, 0.5, 1.0, 1.5, 2.0], dtype=jnp.float64)
        npt.assert_allclose(np.array(x_id(xs)), np.array(xs), atol=1e-13)

    def test_invalid_f_type(self):
        """chebfun with non-callable, non-scalar should raise TypeError."""
        with pytest.raises(TypeError, match="Cannot construct"):
            chebfun([1, 2, 3])

    def test_invalid_domain(self):
        """chebfun with a degenerate domain should raise ValueError."""
        with pytest.raises(ValueError):
            chebfun(jnp.sin, domain=(1.0, 1.0))

    def test_f_none_raises(self):
        """chebfun(None) should raise ValueError."""
        with pytest.raises(ValueError, match="f=None"):
            chebfun(None)

    def test_multipiece_domain(self):
        """chebfun on a piecewise domain produces multiple pieces."""
        f = chebfun(jnp.sin, domain=[-1, 0, 1])
        assert len(f.funs) == 2


